        <p class="news_onpage_paragraph">
            Вскоре в продажу поступит новый бренд кофе – Grumppuccino, в рекламной кампании которого кот Соус Тардар примет активное участие. Сообщение о новинке на рынке появилось на личном сайте звезды интернета "Сердитого кота".
        </p>
        <p class="news_onpage_paragraph">
            Кофе планируют продавать онлайн-магазинах. Он будет в трех видах – ванильный, мокко и черный.
        </p>
        <p class="news_onpage_paragraph">
            Хозяйка кота уже придумала слоган, который будет красоваться на бутылочке с фотографией Соус Тардара, - "Это ужасно хорошо".
        </p>
        <p class="news_onpage_paragraph">
            Отметим, что мировую известность Соус Тардар получила после того, как ее фото появились в интернете. На самом деле "Сердитый кот" является кошкой, которая имеет нарушения в генетике, что привело к неправильному прикусу. Однако именно это и стало причиной огромной популярности. Известно, что она живет в Аризоне. Страница интернет любимца на одном из видеоресурсов набрала более 15 млн просмотров.
        </p>
        <p class="news_onpage_paragraph">
            Ее показали в нескольких популярных телешоу, с ней фотографируются Lady Gaga и Ким Кардашьян. К животному ежедневно приезжают фанаты, но хозяева дают только посмотреть - трогать запрещено. Потому что Сердитая кошка - это настоящий капитал. Календари и футболки с ней имеют бешеный спрос, а скоро о ней получится целая книга.
        </p>
        <p class="news_onpage_paragraph">
            На фотке тот самый котик.
